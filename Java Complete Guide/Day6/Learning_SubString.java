package Day6;

public class Learning_SubString {
    public static void main(String[] args) {
        String message = "Welcome to Java Training";
        System.out.println(message.substring(0,7));
        System.out.println(message.substring(8));
        System.out.println(message.substring(5,6));

        String str = new String("Welcome Home");
        System.out.println(str.substring(7));

    }
}
